<s docid="AP891017-0195" num="21" wdcount="13"> The Richter scale is a measure of ground motion as recorded on seismographs.</s>

<s docid="AP891017-0195" num="22" wdcount="11"> Every increase of one number means a tenfold increase in magnitude.</s>

<s docid="AP891017-0195" num="23" wdcount="15"> Thus a reading of 7.5 reflects an earthquake 10 times stronger than one of 6.5.</s>

