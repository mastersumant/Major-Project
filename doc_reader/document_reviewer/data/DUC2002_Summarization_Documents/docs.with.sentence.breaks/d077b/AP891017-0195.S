<s docid="AP891017-0195" num="10" wdcount="27"> A major earthquake rocked northern California Tuesday evening, collapsing part of the San Francisco Bay Bridge and shaking Candlestick Park and buildings up to 95 miles away.</s>

<s docid="AP891017-0195" num="11" wdcount="9"> Some buildings may have collapsed, said Police Officer D.</s>

<s docid="AP891017-0195" num="12" wdcount="29"> Collins. There were reports of injuries at Candlestick, where fans were waiting for the World Series to begin, and the stadium was evacuated, ABC-TV and Cable News Network reported.</s>

<s docid="AP891017-0195" num="13" wdcount="24"> The quake, which lasted about 15 seconds, measured 6.5 on the Richter scale, according to seismologists at the California Institute of Technology in Pasadena.</s>

<s docid="AP891017-0195" num="14" wdcount="12"> The preliminary reading was from the Alaska Tsunami warning system, Caltech said.</s>

<s docid="AP891017-0195" num="15" wdcount="21"> Televised footage of the bridge showed that a 30-foot section of the span's upper deck had collapsed onto the bottom level.</s>

<s docid="AP891017-0195" num="16" wdcount="12"> Fans at Candlestick screamed as the stadium swayed for about 15 seconds.</s>

<s docid="AP891017-0195" num="17" wdcount="24"> Players from the San Francisco Giants and the Oakland Athletics were on the field and stood around as fans began to leave their seats.</s>

<s docid="AP891017-0195" num="18" wdcount="7"> Buildings in San Francisco swayed several feet.</s>

<s docid="AP891017-0195" num="19" wdcount="25"> The quake shook buildings in Sacramento, 95 miles to the northeast, and was felt as far away as Reno, Nev., 225 miles to the east.</s>

