<s docid="AP900421-0075" num="34" wdcount="40"> By studying stars and galaxies so distant their light has been traveling to Earth for 14 billion years, they hope to determine the precise origin of the universe and learn whether other solar systems _ and life forms _ exist.</s>

<s docid="AP900421-0075" num="35" wdcount="13"> ``If everything goes well, it's going to knock our socks off,'' Chaisson said.</s>

<s docid="AP900421-0075" num="36" wdcount="24"> NASA's sole attempt to launch Discovery was halted four minutes before liftoff April 10 when one of the shuttle's three auxiliary power units failed.</s>

<s docid="AP900421-0075" num="37" wdcount="23"> The 88-pound unit was replaced with a new one, the first time NASA ever had installed such a unit at the launch pad.</s>

<s docid="AP900421-0075" num="38" wdcount="22"> The procedure went so smoothly the space agency decided last week to advance the launch to Tuesday, a day earlier than planned.</s>

<s docid="AP900421-0075" num="39" wdcount="15"> Hubble's six nickel-hydrogen batteries were returned to the telescope Friday following 130 hours of recharging.</s>

<s docid="AP900421-0075" num="40" wdcount="33"> The batteries will power Hubble from the time it is disconnected from Discovery's power system until its energy-collecting solar panels are deployed 380 miles above Earth, the highest a shuttle has ever gone.</s>

<s docid="AP900421-0075" num="41" wdcount="24"> Astronaut Steven A. Hawley will use the shuttle's mechanical arm to lift the 24,250-pound telescope into space on the second day of the mission.</s>

