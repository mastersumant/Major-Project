<s docid="AP900421-0075" num="12" wdcount="35"> Two weeks after a failed launch attempt, an improved Discovery is ready to embark on one of the most momentous shuttle missions, hoisting the Hubble Space Telescope high enough to behold the threshold of time.</s>

<s docid="AP900421-0075" num="13" wdcount="14"> The countdown began Saturday afternoon for a launch set for 8:31 a.m. EDT Tuesday.</s>

<s docid="AP900421-0075" num="14" wdcount="21"> If the launch goes on schedule and all else goes well, the first picture from Hubble could be available next weekend.</s>

<s docid="AP900421-0075" num="15" wdcount="7"> ``We are as excited, maybe even more.</s>

<s docid="AP900421-0075" num="16" wdcount="25"> You can cut it with a knife, incredible,'' said Eric Chaisson, senior scientist at the Space Telescope Science Institute at Johns Hopkins University in Baltimore.</s>

<s docid="AP900421-0075" num="17" wdcount="29"> Nothing, at least scientifically, seems to have been lost as a result of the two-week wait, one of numerous delays plaguing the mission since its original 1983 launch date.</s>

<s docid="AP900421-0075" num="18" wdcount="14"> As Chaisson remarked last week: ``The universe has been around for 15 billion years.</s>

<s docid="AP900421-0075" num="19" wdcount="9"> A couple of weeks is not going to matter''.</s>

<s docid="AP900421-0075" num="20" wdcount="7"> ``The heavens look the same to me.</s>

<s docid="AP900421-0075" num="21" wdcount="7"> The secrets are the same,'' Chaisson said.</s>

<s docid="AP900421-0075" num="22" wdcount="14"> ``There's not a bit of difference,'' agreed Charles Pellerin Jr., NASA's director of astrophysics.</s>

<s docid="AP900421-0075" num="23" wdcount="15"> ``I think the main point is the Hubble Space Telescope still is in perfect condition''.</s>

<s docid="AP900421-0075" num="24" wdcount="21"> The $1.5 billion telescope, the most expensive unmanned spacecraft ever built, rests safely inside Discovery's payload bay awaiting the Tuesday launch.</s>

<s docid="AP900421-0075" num="25" wdcount="22"> If all goes well, Discovery will return to Edwards Air Force Base, Calif., on Sunday, April 29, with an empty payload bay.</s>

<s docid="AP900421-0075" num="26" wdcount="28"> NASA said it may release its first telescope image from Hubble _ of an open star cluster in the constellation Carina _ as early as that same day.</s>

<s docid="AP900421-0075" num="27" wdcount="29"> After the early test images are made and other preparatory work is done, Hubble is expected to start providing significant scientific data by satellite in a month or two.</s>

<s docid="AP900421-0075" num="28" wdcount="17"> The observatory is considered the greatest advance in astronomy since Galileo first used a telescope in 1609.</s>

<s docid="AP900421-0075" num="29" wdcount="22"> It also is considered by many at NASA to be the most important shuttle mission to date; it will be the 35th.</s>

<s docid="AP900421-0075" num="30" wdcount="21"> The telescope is named for the late astronomer Edwin P. Hubble, who discovered during the 1920s the universe constantly is expanding.</s>

<s docid="AP900421-0075" num="31" wdcount="20"> His work gave rise to the theory the universe was created by a tremendous explosion about 15 billion years ago.</s>

<s docid="AP900421-0075" num="32" wdcount="30"> From its perch far above Earth's obstructing atmosphere, the telescope will be able to discern objects 50 times fainter and with 10 times greater clarity than the best ground-based observatory.</s>

