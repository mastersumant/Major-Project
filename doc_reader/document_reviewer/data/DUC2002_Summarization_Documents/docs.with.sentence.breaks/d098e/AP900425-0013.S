<s docid="AP900425-0013" num="19" wdcount="35"> For scientists, and then the rest of us, all that becomes possible now that the space shuttle Discovery has carried the Hubble Space Telescope into the sky to see what no one has seen before.</s>

<s docid="AP900425-0013" num="20" wdcount="22"> The telescope, a silvery tube as long as a tanker truck, has an unmatched ability to detect and capture the faintest light.</s>

<s docid="AP900425-0013" num="21" wdcount="13"> Light, speeding along at 186,000 miles a second, is the messenger of creation.</s>

