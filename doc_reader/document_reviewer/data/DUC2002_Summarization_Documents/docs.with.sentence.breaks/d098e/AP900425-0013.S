<s docid="AP900425-0013" num="23" wdcount="16"> The Hubble promises to give science an open window perhaps to the first echoes of creation.</s>

<s docid="AP900425-0013" num="24" wdcount="12"> This is a world-class event, even if astronomy is not your thing.</s>

