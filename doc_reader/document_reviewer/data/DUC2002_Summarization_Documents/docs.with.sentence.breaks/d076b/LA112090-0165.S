</P>
</HEADLINE>
<TEXT>
<P>

<s docid="LA112090-0165" num="8" wdcount="25"> Flamboyant former Defense Minister Michael Heseltine's challenge to Prime Minister Margaret Thatcher for leadership of the Conservative Party has caused a political sensation in Britain.</s>

<s docid="LA112090-0165" num="9" wdcount="54"> A crucial first vote of Tory Members of Parliament (MPs) is scheduled for today, and if Heseltine should wrest control of the party from Thatcher -- the longest-serving British head of government in modern times -- she would also have to give up the prime minister's job that she has held since 1979 .</s>
</P>
<P>

<s docid="LA112090-0165" num="10" wdcount="13"> Here's a sampling of editorial comment on the contest that has stirred Britain:</s>
