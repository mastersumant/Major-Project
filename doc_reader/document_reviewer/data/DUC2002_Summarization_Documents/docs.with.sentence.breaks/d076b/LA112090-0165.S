<s docid="LA112090-0165" num="12" wdcount="14"> . . . It is more likely than not that she is finished altogether.</s>

<s docid="LA112090-0165" num="13" wdcount="11"> The coup de grace may not be delivered at this moment.</s>

<s docid="LA112090-0165" num="14" wdcount="12"> The nature of the era that comes afterward is far from clear.</s>

<s docid="LA112090-0165" num="15" wdcount="23"> But as a political force, she is condemned at best to an unrelenting rear-guard action which will end sometime in a humiliating abyss.</s>

<s docid="LA112090-0165" num="16" wdcount="3"> . . .</s>
</P>
<P>

<s docid="LA112090-0165" num="17" wdcount="40"> "The party may sullenly support her now, but they will ask the question again in the spring, knowing that if they do not do so the country will ask the same question even more pitilessly when the general election comes.</s>

<s docid="LA112090-0165" num="18" wdcount="3"> . . .</s>
</P>
