</HEAD>
<BYLINE>

<s docid="AP891106-0072" num="6" wdcount="3"> By LESLIE SHEPHERD</s>
</BYLINE>
<BYLINE>

<s docid="AP891106-0072" num="7" wdcount="3"> Associated Press Writer</s>
