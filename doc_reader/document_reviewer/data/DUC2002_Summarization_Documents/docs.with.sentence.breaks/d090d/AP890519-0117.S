<s docid="AP890519-0117" num="12" wdcount="14"> Hospital officials said the ousted leader was suffering from renal, cardiac and pulmonary failure.</s>

<s docid="AP890519-0117" num="13" wdcount="30"> Marcos' wife, Imelda, broadcast an appeal Friday to Mrs. Aquino to allow her husband to die in his homeland, and his supporters threatened mass protests if the request is rejected.</s>

<s docid="AP890519-0117" num="14" wdcount="29"> But Mrs. Aquino refused to withdraw her ban on Marcos, and the Philippine Supreme Court rejected a petition from the former president's lawyer, Juan David, to order the return.</s>

