<s docid="AP890519-0117" num="26" wdcount="36"> ``My countrymen, I am appealing to each and every one of you to pray that, hopefully, Mrs. Aquino will know that bringing home Marcos will bring true reconciliation, true peace and unity for our country,'' Mrs.</s>

