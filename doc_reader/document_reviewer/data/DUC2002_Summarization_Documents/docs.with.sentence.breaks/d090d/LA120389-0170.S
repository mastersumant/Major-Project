<P>

<s docid="LA120389-0170" num="7" wdcount="10"> PHILIPPINES: CORAZON AQUINO'S VICE PRESIDENT REFUSES TO CONDEMN THE INSURRECTION.</s>

<s docid="LA120389-0170" num="8" wdcount="11"> AND HE PREDICTS THAT IT MAY LEAD TO CIVIL WAR .</s>
