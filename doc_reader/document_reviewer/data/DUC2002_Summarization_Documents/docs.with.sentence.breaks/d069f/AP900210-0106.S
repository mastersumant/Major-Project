<s docid="AP900210-0106" num="10" wdcount="23"> About 20,000 East Germans, many carrying West German flags, demonstrated Saturday for speedy German reunification, the official East German news agency ADN said.</s>

<s docid="AP900210-0106" num="11" wdcount="34"> As the demonstrators rallied in the southern city of Plauen, West German Chancellor Helmut Kohl said in Moscow that Soviet President Mikhail S. Gorbachev had given him an unequivocal promise to respect German reunification.</s>

<s docid="AP900210-0106" num="12" wdcount="9"> Kohl declared it a ``very good day for Germany''.</s>

