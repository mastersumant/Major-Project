<s docid="AP900210-0106" num="14" wdcount="26"> Calls for German reunification have increased since the ouster of the hard-line Communist leadership and the opening of East Germany's borders during the fall pro-democracy movement.</s>

<s docid="AP900210-0106" num="15" wdcount="26"> Despite reforms, nearly 2,000 East Germans, most of them skilled workers, continue to leave their homeland daily, crippling an economy already troubled by strikes and absenteeism.</s>

<s docid="AP900210-0106" num="16" wdcount="31"> Earlier Saturday, West German Finance Minister Theo Waigel said the East German economy was in a ``sorrowful state'' and urged the East German government to release details on its financial situation.</s>

<s docid="AP900210-0106" num="17" wdcount="20"> Waigel's comments came after a day after a high-ranking official in Bonn warned that East Germany is rapidly approaching insolvency.</s>

<s docid="AP900210-0106" num="18" wdcount="12"> ``Things are escalating,'' Waigel said in an interview with West Berlin radio.</s>

<s docid="AP900210-0106" num="19" wdcount="7"> ``The economy is in a sorrowful state.</s>

