<s docid="AP880917-0095" num="28" wdcount="27"> By midweek, the storm should be in the lower Ohio River Valley and end up in the Great Lakes as nothing more than foul weather, forecasters said.</s>

<s docid="AP880917-0095" num="29" wdcount="12"> ``The people in the Midwest better dust off their umbrellas,'' Zimmer said.</s>

<s docid="AP880917-0095" num="30" wdcount="38"> Gilbert, responsible for billions of dollars in damage during a weeklong rampage across the Caribbean and Gulf of Mexico, barreled ashore Friday afternoon in Tamaulipas state, a sparsely populated area of Mexico about 120 miles south of Brownsville.</s>

<s docid="AP880917-0095" num="31" wdcount="33"> The eye passed over Santa Rosa and Santa Cruz, two small villages a few miles north of La Pesca, Mexico, where ocean tides coursed over two miles of flatlands and into the town.</s>

<s docid="AP880917-0095" num="32" wdcount="17"> ``We presume (the Mexican villages) are getting a good pounding from 120 mph winds,'' Zimmer said Friday.</s>

<s docid="AP880917-0095" num="33" wdcount="24"> Thousands of people evacuated in areas of northeastern Mexico, including the entire beach area of Matamoros, a city of 280,000 just across the border.</s>

<s docid="AP880917-0095" num="34" wdcount="31"> To the south, about 300,000 people were homeless in Yucatan state and 10,000 more were homeless in Campeche, on the west coast of the peninsula, the government news agency Notimex said.</s>

