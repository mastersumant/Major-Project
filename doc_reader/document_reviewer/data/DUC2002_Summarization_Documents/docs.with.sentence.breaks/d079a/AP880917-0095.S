<s docid="AP880917-0095" num="11" wdcount="29"> Hurricane Gilbert weakened to a tropical storm as it blustered northwestward today but it threw tornadoes and sheets of rain at thousands of shuttered evacuees along the Texas-Mexico border.</s>

<s docid="AP880917-0095" num="12" wdcount="40"> At least 109 people have been killed and more than 800,000 left homeless since Gilbert ripped through the Caribbean with 175 mph wind earlier this week, then crossed the Yucatan Peninsula and Gulf of Mexico before slamming into northeastern Mexico.</s>

