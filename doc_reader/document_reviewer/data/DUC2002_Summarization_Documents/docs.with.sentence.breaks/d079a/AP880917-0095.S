<s docid="AP880917-0095" num="14" wdcount="30"> But by this morning, the once massive storm's maximum sustained winds had flagged to 65 mph, nine less than hurricane strength, and forecasters said the eye had all but disappeared.</s>

<s docid="AP880917-0095" num="15" wdcount="10"> However, they warned of continued danger from flash flooding inland.</s>

<s docid="AP880917-0095" num="16" wdcount="39"> Gilbert was downgraded to a tropical storm at 6 a.m. EDT as it proceeded 50 miles southeast of the city of Monterrey in northern Mexico, said Noel Rishnychok, a meteorologist with the National Hurricane Center in Coral Gables, Fla.</s>

<s docid="AP880917-0095" num="17" wdcount="14"> Winds were still weakening as the storm moved west-northwest at 12 mph, he said.</s>

<s docid="AP880917-0095" num="18" wdcount="34"> Gulf Coast residents expressed relief that the storm, which earlier was deemed the most powerful ever tracked in the Western Hemisphere, pulled its punch as it came ashore despite delivering a spate of tornadoes.</s>

<s docid="AP880917-0095" num="19" wdcount="9"> ``We're thankful,'' said Betty Turner, mayor of Corpus Christi.</s>

<s docid="AP880917-0095" num="20" wdcount="17"> ``What could have been one of the most deadly storms in history appears to have missed us''.</s>

<s docid="AP880917-0095" num="21" wdcount="21"> Corpus Christi residents who evacuated were eager to return, but Turner said, ``We're saying don't do it because of the flooding''.</s>

<s docid="AP880917-0095" num="22" wdcount="29"> At least a dozen tornadoes peppered southern Texas today, following 14 on Friday, killing one woman, striking a hospital complex, military bases and a mobile home park, authorities said.</s>

<s docid="AP880917-0095" num="23" wdcount="4"> Some injuries were reported.</s>

<s docid="AP880917-0095" num="24" wdcount="36"> At 6 a.m. CDT, the center of the storm was in northern Mexico near latitude 25.0 degrees north and longitude 99.8 west, or about 150 miles southwest of Brownsville, located at the southernmost tip of Texas.</s>

