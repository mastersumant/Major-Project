<s docid="FT923-6038" num="13" wdcount="19"> By yesterday afternoon, it had been down-graded to tropical storm, in that its sustained windspeeds were below 75 mph.</s>

<s docid="FT923-6038" num="14" wdcount="31"> Initial reports said at least one person had died, 75 been injured and thousands made homeless along the Louisiana coast, after 14 confirmed deaths in Florida and three in the Bahamas.</s>

<s docid="FT923-6038" num="15" wdcount="21"> The storm caused little damage to Louisiana's important oil-refining industry, although some plants had to halt production when electricity was cut.</s>

<s docid="FT923-6038" num="16" wdcount="20"> The Lloyd's member, in close contact with leading insurers in Florida, said that damage to insured property was remarkably small.</s>

<s docid="FT923-6038" num="17" wdcount="20"> More than Dollars 15bn of damage may have been caused in all, but was mostly to uninsured property, he said.</s>

<s docid="FT923-6038" num="18" wdcount="6"> In north Miami, damage is minimal.</s>

