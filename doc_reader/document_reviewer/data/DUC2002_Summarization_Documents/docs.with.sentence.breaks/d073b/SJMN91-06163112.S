<s docid="SJMN91-06163112" num="28" wdcount="26"> Planes flying through clouds of volcanic ash -- indistinguishable on radar from rain clouds -- run the risk of having their engines stalled by the grit.</s>

<s docid="SJMN91-06163112" num="29" wdcount="11"> So far, the winds have blown ash away from the base.</s>

