<s docid="SJMN91-06163112" num="31" wdcount="31"> The volcano's activity -- measured by seismometers detecting slight earthquakes in its molten rock plumbing system -- is increasing in a way that suggests a large eruption is imminent, Lipman said.</s>

<s docid="SJMN91-06163112" num="32" wdcount="18"> The scientists also are monitoring tiltmeters on the volcano's slopes, to watch it swell as magma builds inside.</s>

