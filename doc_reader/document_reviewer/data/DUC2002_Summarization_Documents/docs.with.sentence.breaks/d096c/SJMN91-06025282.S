<s docid="SJMN91-06025282" num="15" wdcount="16"> Other events will include a salute to black sports legends and a Doug Williams golf tournament.</s>

<s docid="SJMN91-06025282" num="16" wdcount="31"> 'Much more involvement'; This year, the Super Bowl Task Force, a group headed by two of the city's most influential white businessmen, named two black men to head a community-relations committee.</s>

<s docid="SJMN91-06025282" num="17" wdcount="19"> But perhaps most important, Carley said, several minority-owned businesses have won lucrative contracts from the NFL for the game.</s>

<s docid="SJMN91-06025282" num="18" wdcount="20"> "There's much more involvement this time, black and white," said Carley, who is the affirmative-action officer for Hillsboro Community College.</s>

