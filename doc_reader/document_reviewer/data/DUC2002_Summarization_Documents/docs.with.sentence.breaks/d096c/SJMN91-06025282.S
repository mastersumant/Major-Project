<s docid="SJMN91-06025282" num="20" wdcount="42"> In 1984, there was not even a thought given to a black man."; About 100,000 visitors, many of them corporate and political movers and shakers, were expected to arrive in Tampa this week for the festivities leading up to the game Sunday.</s>

<s docid="SJMN91-06025282" num="21" wdcount="34"> The game itself, which is televised to 750 million people around the world, is only part of an event whose impact on a city has been compared to that of a national political convention.</s>

