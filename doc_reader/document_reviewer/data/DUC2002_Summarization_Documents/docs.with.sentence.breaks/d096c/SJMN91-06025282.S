<s docid="SJMN91-06025282" num="27" wdcount="44"> A week before the Super Bowl in Miami two years ago, the fatal shooting of a black man on a motorcycle by an Hispanic police officer in the predominantly black Overtown neighborhood set off three days of looting and torchings of buildings and cars.</s>

<s docid="SJMN91-06025282" num="28" wdcount="31"> While the disturbances were not caused by the Super Bowl, the NFL was criticized at the time for not making enough of an effort to include minority-group members in the festivities.</s>

