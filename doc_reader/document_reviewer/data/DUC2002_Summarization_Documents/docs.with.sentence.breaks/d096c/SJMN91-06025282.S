<s docid="SJMN91-06025282" num="30" wdcount="44"> In Tampa, Super Bowl XXV brought about the cancellation of what was the biggest annual event in the city for 86 years: the Gasparilla Parade, an extravaganza that featured 500 of the city's most powerful men swaggering through the streets dressed as gun-toting pirates.</s>

<s docid="SJMN91-06025282" num="31" wdcount="16"> Tampa officials made the parade part of their pitch to the NFL for the Super Bowl.</s>

<s docid="SJMN91-06025282" num="32" wdcount="34"> But after Tampa was awarded the 1991 Super Bowl, a racial dispute erupted over the parade, which always has been sponsored and organized by an all-white, all-male private club, Ye Mystic Krewe of Gasparilla.</s>

<s docid="SJMN91-06025282" num="33" wdcount="21"> The club's top officials refused the demands of a coalition of black residents, including Carley, that the club be integrated immediately.</s>

