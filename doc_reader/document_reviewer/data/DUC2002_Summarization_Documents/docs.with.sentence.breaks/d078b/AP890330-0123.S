<s docid="AP890330-0123" num="9" wdcount="18"> Spencer Tracy's acting technique was simply ``less is better,'' and that helped make him the screen's greatest actor.</s>

<s docid="AP890330-0123" num="10" wdcount="12"> The Tracy credo is recommended to future producers of Academy Award shows.</s>

<s docid="AP890330-0123" num="11" wdcount="14"> Wednesday night's orgy of self-congratulation was dazzling, glamorous and all those familiar Hollywood adjectives.</s>

<s docid="AP890330-0123" num="12" wdcount="3"> Also too much.</s>

<s docid="AP890330-0123" num="13" wdcount="5"> Too many endless production numbers.</s>

<s docid="AP890330-0123" num="14" wdcount="6"> Too much forced conversation between couples.</s>

<s docid="AP890330-0123" num="15" wdcount="10"> Too many awards with meaning lost to the general public.</s>

<s docid="AP890330-0123" num="16" wdcount="23"> The use of ``couples, co-stars, compadres and companions,'' as presenters for the 61st Academy Awards at the Shrine Auditorium was clever but wearing.</s>

<s docid="AP890330-0123" num="17" wdcount="27"> The best Oscar shows have been emceed by Will Rogers, Bob Hope, Johnny Carson and other funnymen who provided continuity as well as a leavening of pomposity.</s>

<s docid="AP890330-0123" num="18" wdcount="22"> The brightest moments of the 61st Awards came from Billy Crystal, Robin Williams and Bob Hope, but they were all too brief.</s>

<s docid="AP890330-0123" num="19" wdcount="17"> Tom Selleck remarked that Douglas Fairbanks Sr. handed out the first awards in 1929 in five minutes.</s>

<s docid="AP890330-0123" num="20" wdcount="23"> The present awards could be staged in two hours instead of the 3 hours and 15 minutes they took Wednesday night on ABC-TV.</s>

<s docid="AP890330-0123" num="21" wdcount="7"> But the show will never be shorter.</s>

<s docid="AP890330-0123" num="22" wdcount="14"> Reason: The time is needed to sell all those 30-second adds at $375,000 per.</s>

