<s docid="AP890330-0123" num="24" wdcount="6"> The most intelligent acceptance: Jodie Foster.</s>

<s docid="AP890330-0123" num="25" wdcount="7"> Warmest reception: Lucille Ball and Bob Hope.</s>

<s docid="AP890330-0123" num="26" wdcount="5"> Best looking legs: Cyd Charisse.</s>

<s docid="AP890330-0123" num="27" wdcount="5"> Second-best looking legs: Martin Short.</s>

<s docid="AP890330-0123" num="28" wdcount="13"> Worst desecration of an icon: Snow White portrayed as a whining would-be starlet.</s>

<s docid="AP890330-0123" num="29" wdcount="11"> Best comedy routine: Billy Crystal imitating Yul Brynner as Babe Ruth.</s>

<s docid="AP890330-0123" num="30" wdcount="13"> Best walk-on music: ``The Second Time Around'' for Melanie Griffith and Don Johnson.</s>

<s docid="AP890330-0123" num="31" wdcount="6"> Least promising rock singer: Rob Lowe.</s>

