<s docid="AP890425-0013" num="15" wdcount="22"> They came to the Navy as strangers, served the Navy as shipmates and friends, and left the Navy as brothers in eternity.</s>

<s docid="AP890425-0013" num="16" wdcount="15"> In the finest Navy tradition, they served proudly on a great battleship, the USS Iowa''.</s>

<s docid="AP890425-0013" num="17" wdcount="29"> The president, a World War II Navy pilot, said he was proud to recommission the Iowa in 1984 and said it had earned 11 battle stars in two wars.</s>

<s docid="AP890425-0013" num="18" wdcount="31"> But with the still-unexplained fire and explosion last Wednesday in the battleship's No. 2 gun turret, he said, ``Fate has written a sorrowful chapter in the history of this great ship''.</s>

<s docid="AP890425-0013" num="19" wdcount="34"> The Iowa's commander, Capt. Fred P. Moosally, said the turret's crew was the best gun crew on the ship, and called its chief _ Chief Gunner's Mate Reginald Owen Ziegler of Port Gibson, N.Y.</s>

