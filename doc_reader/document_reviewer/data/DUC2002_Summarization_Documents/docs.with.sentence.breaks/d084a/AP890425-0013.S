<s docid="AP890425-0013" num="11" wdcount="31"> Thousands of grieving friends and relatives of the fallen 47 crewmen from the battleship USS Iowa gathered quietly Monday and heard President Bush praise their loved ones as ``brothers in eternity''.</s>

<s docid="AP890425-0013" num="12" wdcount="30"> ``We join today in mourning for the 47 who perished, and in fact, for the 11 who survived,'' the president told a packed memorial service at Norfolk Naval Air Station.</s>

<s docid="AP890425-0013" num="13" wdcount="14"> ``They all were, in the words of a poet, `the men behind the guns'.</s>

