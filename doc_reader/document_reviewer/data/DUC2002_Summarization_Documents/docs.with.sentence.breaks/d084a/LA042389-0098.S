<s docid="LA042389-0098" num="10" wdcount="58"> Air Force spokesman Capt. Walter Thorp said Saturday it will take as long as 10 days to complete the pathology process on the remaining 35 sailors who died when gunpowder exploded in the No. 2 turret of the Iowa on Wednesday, as the World War II-era battleship was conducting an exercise 200 miles northeast of Puerto Rico .</s>
</P>
<P>

<s docid="LA042389-0098" num="11" wdcount="24"> Although autopsies are not conducted at the base as a matter of policy, the Navy requested forensics exams for each of the sailors .</s>
