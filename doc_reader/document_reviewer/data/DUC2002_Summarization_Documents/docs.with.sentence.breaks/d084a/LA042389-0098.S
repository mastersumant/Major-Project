<s docid="LA042389-0098" num="9" wdcount="40"> A dozen of the 47 sailors killed in an explosion aboard the battleship Iowa have undergone pathology exams, and an official said four of them were dressed in full military uniform and sent to their hometowns Saturday for burial .</s>
</P>
