<s docid="SJMN91-06238185" num="4" wdcount="33"> IT IS the stuff of a Byzantine Cold War novel, enticing enough to grab the attention of even the most benign conspiracy theorist: Did Mikhail Gorbachev orchestrate the coup that almost overthrew him?</s>

<s docid="SJMN91-06238185" num="5" wdcount="15"> The blueprint for imposing a state of emergency was drawn by Gorbachev himself last winter.</s>

<s docid="SJMN91-06238185" num="6" wdcount="17"> He pushed through the legislation that included suspension of political activity and of freedom of the press.</s>

<s docid="SJMN91-06238185" num="7" wdcount="19"> He hand-picked the heads of the coercive institutions and other key officials who took part in the coup .</s>
</LEADPARA>
