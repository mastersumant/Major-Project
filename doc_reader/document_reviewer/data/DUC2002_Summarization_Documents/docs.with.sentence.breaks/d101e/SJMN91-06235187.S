<s docid="SJMN91-06235187" num="6" wdcount="14"> In Moscow on Wednesday, the system the Bolsheviks and Joseph Stalin created finally collapsed.</s>

<s docid="SJMN91-06235187" num="7" wdcount="5"> A new historical epoch began.</s>

<s docid="SJMN91-06235187" num="8" wdcount="27"> Soviets had an experience comparable to those that transformed the outlooks of Poles, Hungarians, Czechs and Slovaks in 1989, when they learned they could finally be free.</s>

