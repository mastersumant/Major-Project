<s docid="AP880817-0124" num="45" wdcount="34"> Five strikes in Poland from April 25 to May 10, and brief stoppages or strike threats in 25 more work places, constituted the worst labor unrest in the country since the 1981 martial-law crackdown.</s>

<s docid="AP880817-0124" num="46" wdcount="16"> One of the stoppages, at a steel mill near Krakow, was broken up by security forces.</s>

