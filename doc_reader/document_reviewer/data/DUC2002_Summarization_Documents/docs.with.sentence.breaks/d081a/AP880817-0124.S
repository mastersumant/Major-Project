<s docid="AP880817-0124" num="41" wdcount="19"> Kuron said mine management then locked out members of the afternoon shift to prevent them from joining the strike.</s>

<s docid="AP880817-0124" num="42" wdcount="21"> Solidarity spokesmen said workers at the nearby Borynia mine staged a brief sympathy rally but went back to work Tuesday afternoon.</s>

<s docid="AP880817-0124" num="43" wdcount="22"> Also Tuesday, security officials briefly detained 10 Solidarity activists at the Rudna copper mine near the city of Wroclaw in lower Silesia.</s>

