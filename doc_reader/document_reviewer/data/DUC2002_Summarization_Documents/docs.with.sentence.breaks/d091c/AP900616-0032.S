<s docid="AP900616-0032" num="14" wdcount="8"> It's just been devastating,'' he said Friday night.</s>

<s docid="AP900616-0032" num="15" wdcount="22"> Fink said he feared there wouldn't be enough funeral homes to handle the dead in this eastern Ohio town of 4,300 people.</s>

<s docid="AP900616-0032" num="16" wdcount="8"> Allan Hans called the flooding ``the devil's work''.</s>

<s docid="AP900616-0032" num="17" wdcount="13"> ``I lost everything but my car and the clothes I'm wearing,'' he said.</s>

<s docid="AP900616-0032" num="18" wdcount="21"> Hans said he was sitting in his trailer when he heard a branch crack and went to check out the noise.</s>

<s docid="AP900616-0032" num="19" wdcount="12"> He saw rushing water, ran to tell neighbors and moved his car.</s>

