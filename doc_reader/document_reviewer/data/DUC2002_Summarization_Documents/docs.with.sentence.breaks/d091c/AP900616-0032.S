<s docid="AP900616-0032" num="10" wdcount="33"> Cement blocks were all that remained of homes, fallen branches stood where trees used to be and muddy streams meandered through the outskirts of this Ohio River town after a furious flash flood.</s>

<s docid="AP900616-0032" num="11" wdcount="39"> Residents donned shorts and hip-waders Friday to begin putting their lives back together after 5 inches of rain fell in 3 hours late Thursday, churning floodwaters that left at least 11 people dead and 51 others missing early today.</s>

<s docid="AP900616-0032" num="12" wdcount="35"> The shock had not worn off, said police officer Bruce Fink, whose bristly face bore signs of more than 20 hours on duty, including work at a temporary morgue set up at a funeral home.</s>

