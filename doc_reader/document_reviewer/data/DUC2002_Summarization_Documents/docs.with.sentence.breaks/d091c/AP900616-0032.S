<s docid="AP900616-0032" num="21" wdcount="21"> John and Sue Wright, whose home rests on the banks of Pipe Creek, estimated they lost about $20,000 worth of property.</s>

<s docid="AP900616-0032" num="22" wdcount="7"> ``I was one of the lucky ones.</s>

