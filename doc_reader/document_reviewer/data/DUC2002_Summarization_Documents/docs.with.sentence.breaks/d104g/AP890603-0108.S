<s docid="AP890603-0108" num="12" wdcount="23"> April 29 _ Officials meet with student leaders, but independent student groups are not satisfied and continue a class boycott at 41 universities.</s>

<s docid="AP890603-0108" num="13" wdcount="13"> April 30 _ Beijing's Communist Party chief, Zhao Ziyang, meets with student representatives.</s>

<s docid="AP890603-0108" num="14" wdcount="22"> May 4 _ About 100,000 students and supporters march on Tiananmen Square to celebrate the 70th anniversary of China's first student movement.</s>

