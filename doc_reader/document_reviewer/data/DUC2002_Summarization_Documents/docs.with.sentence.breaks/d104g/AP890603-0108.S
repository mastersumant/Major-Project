<s docid="AP890603-0108" num="18" wdcount="16"> May 10 _ Thousands of students bicycle to major media offices in a press freedom demonstration.</s>

<s docid="AP890603-0108" num="19" wdcount="10"> Another protest is held in the provincial capital of Taiyuan.</s>

<s docid="AP890603-0108" num="20" wdcount="13"> May 13 _ About 2,000 students begin a hunger strike in Tiananmen Square.</s>

<s docid="AP890603-0108" num="21" wdcount="12"> May 14 _ Thousands flock to the square to back the students.</s>

