<s docid="AP890603-0108" num="27" wdcount="6"> Journalists and intellectuals join the protest.</s>

<s docid="AP890603-0108" num="28" wdcount="9"> May 17 _ Zhao pleas for students to leave.</s>

<s docid="AP890603-0108" num="29" wdcount="14"> Students reject his appeal and hold marches that generate 1 million supporters in Beijing.</s>

<s docid="AP890603-0108" num="30" wdcount="23"> May 18 _ About 1 million people, including many workers, again take to the streets to show their support for the hunger strikers.</s>

<s docid="AP890603-0108" num="31" wdcount="15"> Li Peng issues a stern lecture to student leaders and refuses to discuss their demands.</s>

<s docid="AP890603-0108" num="32" wdcount="10"> May 19 _ A tearful Zhao visits weakened hunger strikers.</s>

<s docid="AP890603-0108" num="33" wdcount="6"> Li also visits the students briefly.</s>

<s docid="AP890603-0108" num="34" wdcount="9"> The students later decide to end the hunger strike.</s>

<s docid="AP890603-0108" num="35" wdcount="11"> May 20 _ Martial law is declared in parts of Beijing.</s>

<s docid="AP890603-0108" num="36" wdcount="13"> Zhao reportedly resigns after failing to persuade Li and other hardliners to compromise.</s>

