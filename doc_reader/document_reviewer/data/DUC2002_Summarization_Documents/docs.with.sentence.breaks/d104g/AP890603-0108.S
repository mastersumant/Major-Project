<s docid="AP890603-0108" num="38" wdcount="21"> May 21 _ Students claim to reject an order by Li Peng to leave the Tiananman Square or face military action.</s>

<s docid="AP890603-0108" num="39" wdcount="21"> Defying martial law, hundreds of thousands of people remain in the square or block intersections to prevent troops from reaching it.</s>

<s docid="AP890603-0108" num="40" wdcount="28"> May 22 _ Hundreds of thousands of people again block major roads and senior military leaders say they are resisting orders to order their troops on the protesters.</s>

<s docid="AP890603-0108" num="41" wdcount="9"> At least one military convoy is reported to withdraw.</s>

