<s docid="AP890608-0094" num="13" wdcount="40"> April 27 _ In the largest to date of a continuing series of protests, more than 150,000 students, with wide support from people on the street, surge past police lines and fill Tiananmen Square, chanting slogans for democracy and freedom.</s>

<s docid="AP890608-0094" num="14" wdcount="23"> April 29 _ Officials meet with student leaders, but independent student groups are not satisfied and continue a class boycott at 41 universities.</s>

