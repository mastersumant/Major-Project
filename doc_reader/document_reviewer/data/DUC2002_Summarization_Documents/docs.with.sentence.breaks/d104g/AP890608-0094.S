<s docid="AP890608-0094" num="19" wdcount="12"> May 14 _ Thousands flock to the square to back the students.</s>

<s docid="AP890608-0094" num="20" wdcount="6"> The number fasting rises to 3,000.</s>

<s docid="AP890608-0094" num="21" wdcount="13"> May 15 _ The government deadline for students to leave the square passes.</s>

<s docid="AP890608-0094" num="22" wdcount="4"> Soviet President Mikhail S.</s>

<s docid="AP890608-0094" num="23" wdcount="13"> Gorbachev's welcoming ceremony planned near the square must be moved to the airport.</s>

<s docid="AP890608-0094" num="24" wdcount="9"> May 16 _ Hundreds of thousands occupy the square.</s>

<s docid="AP890608-0094" num="25" wdcount="6"> Journalists and intellectuals join the protest.</s>

<s docid="AP890608-0094" num="26" wdcount="9"> May 17 _ Zhao pleads for students to leave.</s>

<s docid="AP890608-0094" num="27" wdcount="14"> Students reject his appeal and hold marches that generate 1 million supporters in Beijing.</s>

<s docid="AP890608-0094" num="28" wdcount="23"> May 18 _ About 1 million people, including many workers, again take to the streets to show their support for the hunger strikers.</s>

<s docid="AP890608-0094" num="29" wdcount="15"> Li Peng issues a stern lecture to student leaders and refuses to discuss their demands.</s>

<s docid="AP890608-0094" num="30" wdcount="10"> May 19 _ A tearful Zhao visits weakened hunger strikers.</s>

<s docid="AP890608-0094" num="31" wdcount="6"> Li also visits the students briefly.</s>

<s docid="AP890608-0094" num="32" wdcount="9"> The students later decide to end the hunger strike.</s>

