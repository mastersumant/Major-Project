<s docid="AP890608-0094" num="50" wdcount="18"> May 31 _ The first of several pro-government rallies is staged by farmers and workers in Beijing suburbs.</s>

<s docid="AP890608-0094" num="51" wdcount="9"> The participants, ordered to show up, show little enthusiasm.</s>

<s docid="AP890608-0094" num="52" wdcount="22"> June 1 _ The Beijing Municipal Government warns foreign journalists that under martial law they must receive approval for all press coverage.</s>

