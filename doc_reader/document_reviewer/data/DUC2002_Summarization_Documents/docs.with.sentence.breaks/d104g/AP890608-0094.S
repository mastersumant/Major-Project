<s docid="AP890608-0094" num="38" wdcount="28"> May 22 _ Hundreds of thousands of people again block major roads and senior military leaders say they are resisting orders to order their troops on the protesters.</s>

<s docid="AP890608-0094" num="39" wdcount="9"> At least one military convoy is reported to withdraw.</s>

