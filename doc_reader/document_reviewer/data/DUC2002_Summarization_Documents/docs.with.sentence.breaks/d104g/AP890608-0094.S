<s docid="AP890608-0094" num="58" wdcount="18"> Troops surround and enter the square June 4 in the early morning hours and thousands of protesters flee.</s>

<s docid="AP890608-0094" num="59" wdcount="6"> Hundreds are killed and thousands wounded.</s>

