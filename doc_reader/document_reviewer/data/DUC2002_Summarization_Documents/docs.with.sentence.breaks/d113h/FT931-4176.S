<s docid="FT931-4176" num="4" wdcount="11"> FT 12 MAR 93 / Indian stock market denies flouting rules</s>
</HEADLINE>
<BYLINE>

<s docid="FT931-4176" num="5" wdcount="6"> By STEFAN WAGSTYL and RC MURTHY</s>
</BYLINE>
<DATELINE>

<s docid="FT931-4176" num="6" wdcount="1"> BOMBAY</s>
