<s docid="AP890107-0039" num="20" wdcount="56"> In the United States, President Reagan said in a statement, ``His Majesty's 62-year reign spanned one of the most tumultuous, and yet at the same time constructive eras in the history of mankind _ an age of unprecedented economic collapse, a most vicious war, astonishing scientific acheivement, and dramatic political and social changes throughout the world.</s>

