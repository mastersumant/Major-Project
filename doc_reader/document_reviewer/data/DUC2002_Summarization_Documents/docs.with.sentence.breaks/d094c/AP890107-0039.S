<s docid="AP890107-0039" num="14" wdcount="20"> Hirohito is the symbolic figure who must assume most and final responsibility for (Japan's) preposterous crimes to the Korean people''.</s>

<s docid="AP890107-0039" num="15" wdcount="32"> Korea was divided into the communist north and the capitalist south in 1945, when U.S. and Soviet troops ended Japan's rule that began in 1910 with Japan's forced annexation of the peninsula.</s>

