<s docid="WSJ881004-0111" num="6" wdcount="18"> The 1984 Summer Olympic Games weren't even over when advertisers began clamoring for pixie gymnast Mary Lou Retton.</s>

<s docid="WSJ881004-0111" num="7" wdcount="21"> She went on to grin relentlessly through some $5 million worth of commercial endorsements -- surely an Olympic record in itself.</s>

<s docid="WSJ881004-0111" num="8" wdcount="22"> But when the 1988 Summer Games ended Sunday night, so, too, did the chance for American athletes to follow in her footsteps.</s>

<s docid="WSJ881004-0111" num="9" wdcount="32"> Ad agency executives, who spend every fourth year glued to their TV sets, hoping to pick out the next star to hawk their clients' products, say that this Olympiad was a dud.</s>

<s docid="WSJ881004-0111" num="10" wdcount="27"> "I suspect this will go down as the dullest in the history of the Olympics," says Jerry Della Femina, chairman of ad agency Della Femina McNamee WCRS.</s>

<s docid="WSJ881004-0111" num="11" wdcount="14"> "This is the first Olympics without passion, and without passion you don't have testimonials.</s>

<s docid="WSJ881004-0111" num="12" wdcount="11"> Without heroes, you don't have anyone that anyone wants to emulate".</s>

<s docid="WSJ881004-0111" num="13" wdcount="18"> On the surface, the Games seemed to offer all the drama that usually makes for lucrative endorsement contracts.</s>

<s docid="WSJ881004-0111" num="14" wdcount="20"> Who could forget diver Greg Louganis smacking his head on the springboard, then coming back to win two gold medals?</s>

<s docid="WSJ881004-0111" num="15" wdcount="17"> Or flamboyant Florence Griffith Joyner, her two-inch fingernails raking the air as she won the 100-meter sprint?</s>

<s docid="WSJ881004-0111" num="16" wdcount="35"> But even these two, along with Jackie Joyner-Kersee, who won the women's heptathalon and the long jump, may find it more difficult than in previous years to turn gold medals into gold in the bank.</s>

<s docid="WSJ881004-0111" num="17" wdcount="15"> The fact is, say many marketers, Olympic gold has lost much of its commercial luster.</s>

<s docid="WSJ881004-0111" num="18" wdcount="28"> "I don't think it's big money like it used to be," says Michael Goldberg of National Media Group Inc., the New York sports-marketing firm that represents Ms. Retton.</s>

<s docid="WSJ881004-0111" num="19" wdcount="18"> "It used to be kind of a knee-jerk reaction to do an ad campaign around an Olympic athlete.</s>

<s docid="WSJ881004-0111" num="20" wdcount="5"> It just doesn't happen anymore".</s>

<s docid="WSJ881004-0111" num="21" wdcount="22"> Certainly, this year's winning Olympians will have no trouble securing contracts from specialized sports-equipment outfitters and speaking invitations from obscure trade groups.</s>

<s docid="WSJ881004-0111" num="22" wdcount="26"> But the major, high-paying endorsements -- such as those with soft-drink companies and fast-food outlets -- just don't seem to be surfacing, at least not yet.</s>

<s docid="WSJ881004-0111" num="23" wdcount="26"> Earlier this year, sports marketers estimated that a gold medal won during the Summer Games could earn a personable athlete more than $1 million in endorsements.</s>

<s docid="WSJ881004-0111" num="24" wdcount="16"> Now, marketers say, these medalists will be lucky to pull in $100,000 to $500,000 a year.</s>

<s docid="WSJ881004-0111" num="25" wdcount="15"> Advertisers can reel off a litany of reasons why these Olympics aren't igniting much spark.</s>

<s docid="WSJ881004-0111" num="26" wdcount="36"> For one thing, they suffer by comparison with the 1984 Summer Games, so exciting because they were held in Los Angeles and because, in the absence of boycotting Soviet-bloc countries, the Americans won 83 gold medals.</s>

<s docid="WSJ881004-0111" num="27" wdcount="23"> The 1988 Games, by contrast, were broadcast from halfway around the world, and the Soviets trounced the Americans, 55 gold medals to 36.</s>

<s docid="WSJ881004-0111" num="28" wdcount="32"> What's more, marketers say, this year's drug scandals cast a pall over the Games, especially the incident involving Canadian sprinter Ben Johnson, who lost his gold medal after testing positive for steroids.</s>

<s docid="WSJ881004-0111" num="29" wdcount="10"> NBC's TV coverage also gets its share of the blame.</s>

<s docid="WSJ881004-0111" num="30" wdcount="24"> Marketers say it was so bland and skipped so frequently from one event to another that there was little chance for excitement to build.</s>

<s docid="WSJ881004-0111" num="31" wdcount="17"> The constant commercial interruptions, which led to ratings that were much lower than expected, didn't help, either.</s>

<s docid="WSJ881004-0111" num="32" wdcount="48"> During most of the past Games, "there was enough continuous TV coverage so that, much like a soap opera, it was possible to get involved in the lives and in the competition," says Leigh Steinberg, attorney for ice-skater Brian Boitano, a gold medalist at the 1988 Winter Games.</s>

<s docid="WSJ881004-0111" num="33" wdcount="6"> But not this summer, he says.</s>

<s docid="WSJ881004-0111" num="34" wdcount="18"> Recent changes in Olympic rules, meanwhile, have led to a glut of Olympians in search of endorsement deals.</s>

<s docid="WSJ881004-0111" num="35" wdcount="19"> Before the 1984 Games, most Olympic athletes weren't allowed to make commercials unless they gave up their amateur status.</s>

<s docid="WSJ881004-0111" num="36" wdcount="23"> Now, they can appear in as many ads as they want, but the income must go into a trust fund for training expenses.</s>

<s docid="WSJ881004-0111" num="37" wdcount="31"> "Every year, more and more Olympic people are out there wanting to do commercials -- basically, everybody is available," says Daisy Sinclair, a vice president with ad agency Ogilvy Group Inc.</s>

<s docid="WSJ881004-0111" num="38" wdcount="15"> As a result, while once an "aura" surrounded an Olympian, now "the bloom is off".</s>

<s docid="WSJ881004-0111" num="39" wdcount="12"> Companies also are becoming more sophisticated in their approach to celebrity endorsements.</s>

<s docid="WSJ881004-0111" num="40" wdcount="21"> Increasingly cost conscious, they will no longer hook up with an Olympian just because the company president likes the athlete's sport.</s>

<s docid="WSJ881004-0111" num="41" wdcount="32"> And after years of paying big bucks to Olympic stars only to see them fade into obscurity, companies have finally gotten the hint that Olympian fame is of the most fleeting variety.</s>

<s docid="WSJ881004-0111" num="42" wdcount="37"> "If you think your phone will ring off the hook if (you win) a gold medal, it isn't going to happen anymore," says Stephen Disson, senior vice president of marketing for ProServ Inc., a Washington sports-marketing firm.</s>

<s docid="WSJ881004-0111" num="43" wdcount="21"> Companies now know that "six months from now there will be other heroes from other sporting events" for them to consider.</s>

<s docid="WSJ881004-0111" num="44" wdcount="11"> Ad agency BBDO Worldwide's attitude toward the Olympics is becoming typical.</s>

<s docid="WSJ881004-0111" num="45" wdcount="25"> It uses sports stars in its commercials for Pepsi Light and Oppenheimer Mutual Funds but has no plans to call on any of 1988's Olympians.</s>

<s docid="WSJ881004-0111" num="46" wdcount="28"> "I prefer to use athletes who are in the public eye on a regular basis, like football and basketball players," says Charles Miesmer, the agency's executive creative director.</s>

<s docid="WSJ881004-0111" num="47" wdcount="27"> "People like Michael Jordan have 10-plus years in their sports careers, all in a row, so I don't have to worry about the public saying, 'Carl who'"?</s>

<s docid="WSJ881004-0111" num="48" wdcount="16"> And don't look for any of 1988's winners to start showing up on Wheaties boxes, either.</s>

<s docid="WSJ881004-0111" num="49" wdcount="36"> General Mills Inc., which has featured Ms. Retton and decathalon gold-medalist Bruce Jenner on its Wheaties boxes in the past, says it looks for three qualities in an endorser: career achievement, popular appeal and off-the-field accomplishments.</s>

<s docid="WSJ881004-0111" num="50" wdcount="13"> A spokeswoman says that none of this year's crop meet all three criteria.</s>

<s docid="WSJ881004-0111" num="51" wdcount="10"> So far, Ms. Joyner-Kersee has had the most visible endorsements.</s>

<s docid="WSJ881004-0111" num="52" wdcount="14"> Even before the Olympics began, she had made commercials for Primatene mist and 7-Up.</s>

<s docid="WSJ881004-0111" num="53" wdcount="33"> And, according to her agent, Pearl Hodge of World Class Management, she is now "in the process" of signing with McDonald's Corp., to work on behalf of its efforts to aid underprivileged children.</s>

<s docid="WSJ881004-0111" num="54" wdcount="12"> Ms. Joyner-Kersee's sister-in-law, Ms. Griffith Joyner, may soon take the endorsement lead.</s>

<s docid="WSJ881004-0111" num="55" wdcount="21"> Advertisers say with her good looks and fashion flare, she is attracting more interest than any of the other medal winners.</s>

<s docid="WSJ881004-0111" num="56" wdcount="8"> (The sprinter's manager couldn't be reached for comment).</s>

<s docid="WSJ881004-0111" num="57" wdcount="14"> Another Olympian with endorsement potential is swimmer Matt Biondi, who won five gold medals.</s>

<s docid="WSJ881004-0111" num="58" wdcount="36"> Mr. Biondi has already signed a swim-wear contract with Arena USA Inc., and Parkes Brittain, his agent at Advantage International Inc., says his client "will definitely be in a high-income ring, the highest you can name".</s>

<s docid="WSJ881004-0111" num="59" wdcount="5"> Then there is Mr. Louganis.</s>

<s docid="WSJ881004-0111" num="60" wdcount="19"> Since winning two gold medals during the 1984 Summer Olympics, the diver has had little luck with major endorsements.</s>

<s docid="WSJ881004-0111" num="61" wdcount="20"> And a few marketing executives say that even his second sweep of the diving events may not up his potential.</s>

<s docid="WSJ881004-0111" num="62" wdcount="29"> "When most advertisers are looking for the male hero, the word 'macho' trails along very closely," says Marty Blackman of Blackman & Raber, a sports-marketing firm in New York.</s>

<s docid="WSJ881004-0111" num="63" wdcount="7"> "I don't find Louganis a macho guy".</s>

<s docid="WSJ881004-0111" num="64" wdcount="7"> Still, other marketing professionals say that Mr.</s>

<s docid="WSJ881004-0111" num="65" wdcount="12"> Louganis's dramatic recovery after smashing his head has secured the diver's future.</s>

<s docid="WSJ881004-0111" num="66" wdcount="10"> His manager, Jim Babbitt, won't comment on any contract offers.</s>

<s docid="WSJ881004-0111" num="67" wdcount="11"> He will say, however, that his client has "done just fine.</s>

<s docid="WSJ881004-0111" num="68" wdcount="3"> He isn't unhappy.</s>

<s docid="WSJ881004-0111" num="69" wdcount="9"> He dives to perform; he doesn't dive for dollars".</s>

