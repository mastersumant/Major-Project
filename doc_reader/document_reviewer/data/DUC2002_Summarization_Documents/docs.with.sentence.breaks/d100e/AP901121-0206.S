<s docid="AP901121-0206" num="36" wdcount="3"> Socks, Clifford said.</s>

<s docid="AP901121-0206" num="37" wdcount="11"> ``Yoko said she couldn't imagine people walking around on John's drawings''.</s>

<s docid="AP901121-0206" num="38" wdcount="13"> Back at the Dakota, Ono receives a familiar question: Is this all commercialism?</s>

<s docid="AP901121-0206" num="39" wdcount="7"> Has the iconoclastic ex-Beatle been sold out?</s>

<s docid="AP901121-0206" num="40" wdcount="2"> She laughs.</s>

