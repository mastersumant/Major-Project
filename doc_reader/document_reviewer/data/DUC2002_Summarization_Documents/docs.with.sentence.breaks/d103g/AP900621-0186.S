<s docid="AP900621-0186" num="16" wdcount="22"> The Armenian quake was measured at 6.9 on the Richter scale, but Thursday's earthquake shook the earth with a magnitude of 7.7.</s>

<s docid="AP900621-0186" num="17" wdcount="38"> The shock wave traveled through the mountainous section of coastal Iran where most of the buildings are built on a flood plain of loosely deposited soil that shifts in an earthquake and allows structures to collapse, he said.</s>

<s docid="AP900621-0186" num="18" wdcount="22"> The 7.7-magnitude quake was the largest ever recorded in that area, where two major plates of the earth's crust meet, Needhams said.</s>

<s docid="AP900621-0186" num="19" wdcount="18"> ``Iran is in a very active seismic region,'' he said, and that there have been many smaller quakes.</s>

