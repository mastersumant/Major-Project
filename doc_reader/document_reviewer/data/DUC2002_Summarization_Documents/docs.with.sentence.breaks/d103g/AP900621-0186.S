<s docid="AP900621-0186" num="8" wdcount="28"> The earthquake that devastated Iran early Thursday took tens of thousands of lives because people were asleep in fragile homes built on flood plains, an earthquake expert said.</s>

<s docid="AP900621-0186" num="9" wdcount="21"> ``Most casualties from earthquakes are caused by something falling on top of you,'' said Russ Needham, a geophysist with the U.S.</s>

<s docid="AP900621-0186" num="10" wdcount="9"> Geological Survey that monitors ground motion around the world.</s>

<s docid="AP900621-0186" num="11" wdcount="27"> The quake hit at 12:30 a.m. in Iran, when most of the people were in their homes, Needham said Thursday in a telephone interview from Golden, Colo.</s>

<s docid="AP900621-0186" num="12" wdcount="31"> At least 25,000 people died and thousands were injured in the quake, which demolished scores of villages and buried thousands of people in the rubble of their homes, Iranian officials said.</s>

<s docid="AP900621-0186" num="13" wdcount="18"> Needham said most structures in that area are built of a ceramic-type brick or adobe that collapses easily.</s>

<s docid="AP900621-0186" num="14" wdcount="22"> ``It's very similar to what we had in Armenia,'' he said of the Dec. 7, 1988, earthquake that killed about 25,000 people.</s>

