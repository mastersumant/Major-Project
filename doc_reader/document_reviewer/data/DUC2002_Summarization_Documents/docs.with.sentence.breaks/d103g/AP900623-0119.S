<s docid="AP900623-0119" num="11" wdcount="16"> The United States sent a message of condolence and expressed its willingness to provide humanitarian aid.</s>

<s docid="AP900623-0119" num="12" wdcount="20"> Iran, which has had no diplomatic ties with the United States since the 1979 seizure of hostages at the U.S.</s>

<s docid="AP900623-0119" num="13" wdcount="18"> Embassy in Tehran, said it would welcome relief from the American Red Cross and other U.S. humanitarian groups.</s>

