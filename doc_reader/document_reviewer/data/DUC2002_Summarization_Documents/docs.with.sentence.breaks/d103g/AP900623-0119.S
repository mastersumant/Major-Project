<s docid="AP900623-0119" num="15" wdcount="23"> An official statement distributed late Friday by the Saudi Press Agency said the kingdom followed with ``deep sorrow'' the news of the tragedy.</s>

<s docid="AP900623-0119" num="16" wdcount="6"> ``The kingdom of Saudi Arabia ...</s>

