<s docid="AP900623-0119" num="19" wdcount="32"> King Hussein sent condolences to Iranian President Hashemi Rafsanjani on Friday in what is believed to be the first direct contact between Jordanian and Iranian heads of state in almost a decade.</s>

<s docid="AP900623-0119" num="20" wdcount="30"> Jordan backed Iraq in the eight-year Iran-Iraq war, which ended with a cease-fire in August 1988, and at one point sent hundreds of Jordanian volunteers to fight alongside Iraqi soldiers.</s>

