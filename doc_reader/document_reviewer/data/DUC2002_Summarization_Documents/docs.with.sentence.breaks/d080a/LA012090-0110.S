<HEADLINE>
<P>

<s docid="LA012090-0110" num="6" wdcount="7"> MCMARTIN LAWSUITS MAY GO ON FOR YEARS</s>
</P>
</HEADLINE>
<BYLINE>
<P>

<s docid="LA012090-0110" num="7" wdcount="6"> By CAROL McGRAW, TIMES STAFF WRITER</s>
</P>
</BYLINE>
<TEXT>
<P>

<s docid="LA012090-0110" num="8" wdcount="39"> Though the historic McMartin Pre-School criminal trial finally has ended in acquittals, a host of civil lawsuits generated by the Manhattan Beach molestation case conceivably could keep the issue alive in the courts for decades, legal experts say .</s>
</P>
<P>

<s docid="LA012090-0110" num="9" wdcount="8"> A confusing array of civil actions await resolution.</s>

<s docid="LA012090-0110" num="10" wdcount="43"> These include a federal civil rights suit filed Friday by Peggy McMartin Buckey against her accusers and others; a lawsuit filed by former defendants against a television station and reporter and suits the pre-school owners have filed against their liability insurance carriers .</s>
</P>
<P>

<s docid="LA012090-0110" num="11" wdcount="19"> Also, Ray Buckey might file a federal suit similar to that of his mother, said his attorney, Scott Bernstein.</s>

<s docid="LA012090-0110" num="12" wdcount="27"> And an attorney representing several parents of McMartin attendees said they might now refile lawsuits against the pre-school for emotional distress allegedly suffered by the children .</s>
</P>
<P>

<s docid="LA012090-0110" num="13" wdcount="21"> "I'm convinced this case will never end," Eli Gauna, who represents Babette Spitler, a former teacher at the school, said wearily.</s>

<s docid="LA012090-0110" num="14" wdcount="18"> "It will always be there, until everyone who was ever involved gets to old age or dies" .</s>
</P>
<P>

<s docid="LA012090-0110" num="15" wdcount="45"> In the criminal case -- the longest and costliest criminal proceeding in U.S. history -- Ray Buckey, 31, and his mother Peggy McMartin Buckey, 63, were acquitted Thursday of 52 counts of molestation involving 11 youngsters who attended the family nursery school in Manhattan Beach.</s>

<s docid="LA012090-0110" num="16" wdcount="15"> The jury deadlocked on 13 remaining counts, and a mistrial was declared on those allegations.</s>

<s docid="LA012090-0110" num="17" wdcount="17"> A decision is expected next week over whether those charges will be refiled against Ray Buckey .</s>
</P>
<P>

<s docid="LA012090-0110" num="18" wdcount="18"> The verdict was a high mark, but not the final moment, in a lengthy and entangled legal saga.</s>

<s docid="LA012090-0110" num="19" wdcount="23"> The criminal case began in March, 1984, when seven McMartin teachers were indicted by a county grand jury on 115 counts of molestation.</s>

<s docid="LA012090-0110" num="20" wdcount="16"> The indictment was superseded by a criminal complaint of 208 counts of molestation and conspiracy .</s>
</P>
<P>

<s docid="LA012090-0110" num="21" wdcount="12"> After an 18-month preliminary hearing, all seven were ordered to stand trial.</s>

<s docid="LA012090-0110" num="22" wdcount="28"> Defendants included Buckey; his mother; his grandmother, Virginia McMartin, who was the school founder; his sister Peggy Ann Buckey; and teachers Spitler, Betty Raidor and Mary Ann Jackson.</s>

<s docid="LA012090-0110" num="23" wdcount="8"> But a week after the hearing ended, Dist.</s>

<s docid="LA012090-0110" num="24" wdcount="15"> Atty. Ira Reiner dropped charges against the latter five defendants citing "incredibly weak evidence" .</s>
</P>
<P>

<s docid="LA012090-0110" num="25" wdcount="15"> Even before the criminal case went to trial, civil litigation already was in the works.</s>

<s docid="LA012090-0110" num="26" wdcount="32"> In 1984, a handful of lawsuits were filed by 22 families seeking monetary damages against the school for medical bills, cost of psychiatric therapy and emotional distress arising from the alleged molestations.</s>

<s docid="LA012090-0110" num="27" wdcount="22"> But those lawsuits later were withdrawn to prevent the children's psychiatric histories from being introduced as evidence in the criminal case .</s>
</P>
<P>

<s docid="LA012090-0110" num="28" wdcount="27"> Greg Mooney, an attorney who represented some of the plaintiffs, explained that during the criminal preliminary hearing, defense attorneys had sought psychiatric records of the alleged victims.</s>

<s docid="LA012090-0110" num="29" wdcount="33"> Defense attorneys argued that they should be entitled to the records because the children had waived their patient-therapist confidentiality by filing civil suits in which their psychiatric histories would be at issue .</s>
</P>
<P>

<s docid="LA012090-0110" num="30" wdcount="19"> "The civil suits were withdrawn so they would not become an issue in the criminal case," Mooney said .</s>
</P>
<P>

<s docid="LA012090-0110" num="31" wdcount="12"> He said Friday, however, that the civil lawsuits can now be refiled.</s>

<s docid="LA012090-0110" num="32" wdcount="25"> Mooney said a decision on whether to refile will be made only after authorities decide what to do with the 13 remaining criminal counts .</s>
</P>
<P>

<s docid="LA012090-0110" num="33" wdcount="13"> "It's impossible to predict how many parents will want to sue," Mooney said.</s>

<s docid="LA012090-0110" num="34" wdcount="8"> "Somewhere between a handful and a bushelful" .</s>
</P>
<P>

<s docid="LA012090-0110" num="35" wdcount="14"> Any new suits would have to be filed only on behalf of the children.</s>

<s docid="LA012090-0110" num="36" wdcount="29"> The parents could not seek damages on their own, because the one-year statute of limitations -- the deadline in which a lawsuit can be filed -- has passed .</s>
</P>
<P>

<s docid="LA012090-0110" num="37" wdcount="16"> Under state law, children who allege abuse have until they are 19 years old to file.</s>

<s docid="LA012090-0110" num="38" wdcount="29"> Many of the children who attended McMartin are now entering their early teens, but the youngest of those who attended the school then is now 8 years old .</s>
</P>
<P>

<s docid="LA012090-0110" num="39" wdcount="20"> Legal experts, however, say some courts have held that child abuse victims can file at any time in their life.</s>

<s docid="LA012090-0110" num="40" wdcount="33"> In the so-called delayed-discovery theory, a statute of limitations does not take effect until the victim actually becomes aware of molestation, and some victims might suppress memories of such attacks for years .</s>
</P>
<P>

<s docid="LA012090-0110" num="41" wdcount="30"> "It's a possibility that hangs over the former defendants," said attorney Gauna, noting a recent landmark California court decision that set aside the statute of limitations for certain victims .</s>
</P>
<P>

<s docid="LA012090-0110" num="42" wdcount="21"> Mooney said he knows of families that, seeking legal advantage, waited for the criminal trial to end before contemplating civil action.</s>

<s docid="LA012090-0110" num="43" wdcount="42"> He explained that some families believed they might have a better advantage in civil court rather than in a criminal case: the burden of proof in civil litigation is the preponderance of the evidence, not reasonable doubt, as in a criminal trial.</s>

<s docid="LA012090-0110" num="44" wdcount="16"> Also, in a civil case, only nine of the 12 jurors must agree on a verdict.</s>

<s docid="LA012090-0110" num="45" wdcount="6"> Criminal convictions require unanimous juries .</s>
</P>
<P>

<s docid="LA012090-0110" num="46" wdcount="18"> Whether the families would be willing to tackle more years of legal proceedings is yet to be seen.</s>

<s docid="LA012090-0110" num="47" wdcount="21"> And some families question whether the McMartin defendants' insurance carriers would pay out to plaintiffs who successfully sued the school .</s>
</P>
<P>

<s docid="LA012090-0110" num="48" wdcount="6"> "There hasn't been any decisions yet.</s>

<s docid="LA012090-0110" num="49" wdcount="20"> The parents need a little space between the events of this week and deciding what they will do," Mooney said.</s>

<s docid="LA012090-0110" num="50" wdcount="12"> "There is some sentiment that says, 'Let's just put this behind us'.</s>

<s docid="LA012090-0110" num="51" wdcount="23"> But there is also sentiment among others that maybe they should pick up the civil case where the criminal case left off .</s>
</P>
<P>

<s docid="LA012090-0110" num="52" wdcount="15"> "They heard the jurors when they said they believed that things happened to the children.</s>

<s docid="LA012090-0110" num="53" wdcount="23"> They think maybe since the criminal jury couldn't get the higher standard of proof, a civil jury would bring about a resolution" .</s>
</P>
<P>

<s docid="LA012090-0110" num="54" wdcount="28"> Said one McMartin attendee's parent, who asked not to be identified: "I might be tempted to file a civil suit to provide a trust fund for my child.</s>

<s docid="LA012090-0110" num="55" wdcount="10"> But there apparently isn't any money there to be had".</s>

<s docid="LA012090-0110" num="56" wdcount="35"> The only other reasons for filing such a suit, the parent added, would be "to work through a lot of emotional things, and we have already done that by participating in the criminal proceedings" .</s>
</P>
<P>

<s docid="LA012090-0110" num="57" wdcount="23"> Gauna, Spitler's attorney, said the litigation will go on because "maybe a half-dozen to a dozen hard-line parents won't let it go away.</s>

<s docid="LA012090-0110" num="58" wdcount="11"> They will live this case until the day they die" .</s>
</P>
<P>

<s docid="LA012090-0110" num="59" wdcount="15"> Meanwhile, already wending its way through court is a lawsuit filed by the early defendants.</s>

<s docid="LA012090-0110" num="60" wdcount="23"> Peggy Ann Buckey, Virginia McMartin, Spitler and Raidor are suing the television station, the reporter who broke the McMartin story and others .</s>
</P>
<P>

<s docid="LA012090-0110" num="61" wdcount="17"> The suit alleges that the station and reporter, Wayne Satz, were "creating rather than reporting the news".</s>

<s docid="LA012090-0110" num="62" wdcount="37"> A lower court held that the station, KABC, was protected from the action on constitutional grounds, but a state appellate court ruled the case should proceed to trial, and the state Supreme Court upheld that decision .</s>
</P>
<P>

<s docid="LA012090-0110" num="63" wdcount="26"> The case, now in the early pretrial stages, is being watched closely, because it could set precedent in the field of press law, experts said .</s>
</P>
<P>

<s docid="LA012090-0110" num="64" wdcount="14"> Another suit pits former McMartin defendants and the pre-school against their liability insurance carriers.</s>

<s docid="LA012090-0110" num="65" wdcount="20"> The McMartin side alleges that the insurance carriers were required by the policy to pay for their attorney bills .</s>
</P>
<P>

<s docid="LA012090-0110" num="66" wdcount="21"> While the insurers have paid a portion of the defense costs, they contend the defendants should have used only one attorney.</s>

<s docid="LA012090-0110" num="67" wdcount="17"> However, the defendants are arguing that because of possible conflicts of interest, each needed their own counsel.</s>

<s docid="LA012090-0110" num="68" wdcount="14"> One of the former defendants, preschool teacher Maryann Jackson, settled out of court .</s>
</P>
<P>

<s docid="LA012090-0110" num="69" wdcount="40"> As it becomes apparent that McMartin cases will stretch out for years to come, parents and the former criminal defendants alike are trying to resign themselves to the inevitability that the matter may be one they can never leave behind.</s>

<s docid="LA012090-0110" num="70" wdcount="15"> Soon, some of the children will be able to make their own legal decisions .</s>
</P>
<P>

<s docid="LA012090-0110" num="71" wdcount="11"> Noted one parent: "It's really not our decision as parents anymore.</s>

<s docid="LA012090-0110" num="72" wdcount="7"> These children will soon be adults" .</s>
</P>
