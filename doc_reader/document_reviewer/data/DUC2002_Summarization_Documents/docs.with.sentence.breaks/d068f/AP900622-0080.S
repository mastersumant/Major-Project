<s docid="AP900622-0080" num="21" wdcount="15"> The United States, France and Great Britain have an estimated 12,000 troops in West Berlin.</s>

<s docid="AP900622-0080" num="22" wdcount="20"> Berlin has been split into Soviet, U.S., British and France sectors and West Berlin remains technically separate from West Germany.</s>

<s docid="AP900622-0080" num="23" wdcount="26"> The German and Allied foreign ministers are meeting in East Berlin to discuss the status of the city and a united Germany's role in world affairs.</s>

