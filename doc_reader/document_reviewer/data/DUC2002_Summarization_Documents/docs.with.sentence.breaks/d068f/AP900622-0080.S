<s docid="AP900622-0080" num="25" wdcount="26"> Large sections of the Berlin Wall are being dismantled daily, and U.S. officials decided to time the removal of Checkpoint Charlie with the foreign ministers' meeting.</s>

<s docid="AP900622-0080" num="26" wdcount="26"> A color guard of French, British and American soldiers strode to the building and locked the doors leading to each Allied nation's compartment in the checkpoint.</s>

