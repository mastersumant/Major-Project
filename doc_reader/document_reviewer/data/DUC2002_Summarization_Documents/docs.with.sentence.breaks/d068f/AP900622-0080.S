<s docid="AP900622-0080" num="11" wdcount="25"> Soldiers from three nations locked the doors of Checkpoint Charlie today and a crane whisked away the Cold War relic that symbolized a divided world.</s>

<s docid="AP900622-0080" num="12" wdcount="2"> A U.S.</s>

<s docid="AP900622-0080" num="13" wdcount="33"> Army band played a stirring song about old Berlin as the rectangular white sentry station, a place East Germans died trying to reach, was hoisted onto a truck for its passage into history.</s>

<s docid="AP900622-0080" num="14" wdcount="30"> The famed Allied checkpoint by the Berlin Wall was closed with an elaborate ceremony that brought together the top diplomats from the Germanys and the four World War II Allies.</s>

<s docid="AP900622-0080" num="15" wdcount="10"> ``For 29 years Checkpoint Charlie embodied the Cold War,'' U.S.</s>

<s docid="AP900622-0080" num="16" wdcount="19"> Secretary of State James A. Baker III told a crowd of invited guests and journalists on Berlin's Friedrich Street.</s>

<s docid="AP900622-0080" num="17" wdcount="22"> ``And now, 29 years after it was built, we meet here today to dismantle it and to bury the conflicts it created''.</s>

<s docid="AP900622-0080" num="18" wdcount="29"> Baker, Soviet Foreign Minister Eduard Shevardnadze and the foreign ministers from France, Britain and the two Germanys each heralded the end of the checkpoint as a symbol of change.</s>

<s docid="AP900622-0080" num="19" wdcount="31"> Shevardnadze called for the removal of Berlin's status as an Allied-controlled city and the withdrawal of troops within six months after the two German states unify, which could come in December.</s>

