<s docid="WSJ900803-0023" num="9" wdcount="36"> Emphasizing that the Central Intelligence Agency wasn't surprised by the Iraqi action, an agency spokesman said yesterday, "We provided policy makers with very useful and timely information on these events and we continue to do so."</s>
