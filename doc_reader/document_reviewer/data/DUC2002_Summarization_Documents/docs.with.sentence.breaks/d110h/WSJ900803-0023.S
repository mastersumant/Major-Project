<s docid="WSJ900803-0023" num="11" wdcount="39"> Since last week, President Bush had been warned by intelligence analysts that what originally looked like a military bluff was quickly turning into the kind of troop deployment that could be used to invade Kuwait or even Saudi Arabia.</s>

<s docid="WSJ900803-0023" num="12" wdcount="21"> Central Intelligence Agency Director William Webster even hand-delivered certain fresh details on the Iraqi buildup to National Security Adviser Brent Scowcroft.</s>

<s docid="WSJ900803-0023" num="13" wdcount="25"> Among the assessments he conveyed was a conclusion that, by this week, President Saddam Hussein's force would be capable of penetrating Kuwait or Saudi Arabia.</s>

